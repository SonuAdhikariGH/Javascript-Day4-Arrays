let sentence = "I love JavaScript";

// Converting the sentence to uppercase
let upperCased = sentence.toUpperCase();
console.log(upperCased);

// Replace the word javascript with coding
let replaced = sentence.replace("JavaScript", "Coding");
console.log(replaced);

// Split the sentence into words of forming an array
let breakedSentences = sentence.split(" ");
console.log(breakedSentences);